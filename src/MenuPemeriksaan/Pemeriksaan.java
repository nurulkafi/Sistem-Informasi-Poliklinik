    private void SimpanData(){
        String NoPen = NoPendaftaran.getSelectedItem().toString().substring(0,5);
        String Keluhan = this.Keluhan.getText();
        String diagn = Diagnosis.getText();
        String prwt = Perawatan.getText();
        String BB = BeratBadan.getText();
        String TD = TensiD.getText();
        String TS = TensiS.getText();
        
        Connection connect = Koneksi.getKoneksi();
            //query untuk memasukan data
        String query2 = "INSERT INTO pemeriksaan (NoPemeriksaan, NoPendaftaran, KodeDokter, Keluhan, Diagnosa,Perawatan,BeratBadan,TensiDiastolik,TensiSistolik) "+"VALUES ('"+KodePemeriksaan+"','"+NoPen+"','"+GlobalVar.id_user+"','"+Keluhan+"','"+diagn+"','"+prwt+"','"+BB+"','"+TD+"','"+TS+"')";
        try{
                //menyiapkan statement untuk di eksekusi

                PreparedStatement ps = (PreparedStatement) connect.prepareStatement(query2);
                ps.executeUpdate(query2);
                if (RBPerlu.isSelected()==true) {
                    String query3 = "INSERT INTO resep (NoResep,NoPemeriksaan) "
                                 + "VALUES ('"+KodeResep+"', '"+KodePemeriksaan+"')";
                    
                    try{
                            //menyiapkan statement untuk di eksekusi
                        PreparedStatement ps3 = (PreparedStatement) connect.prepareStatement(query3);
                        ps3.executeUpdate(query3);
                        for(int a = 0;a<TableResep.getColumnCount();a++){
                            String KodeObat = TableResep.getValueAt(a, 1).toString();
                            String Dosis = TableResep.getValueAt(a, 3).toString();
                            String Jumlah = TableResep.getValueAt(a, 4).toString();
                            String query4 = "INSERT INTO detail_resep (NoResep, KodeObat,Dosis,Jumlah) "
                                     + "VALUES ('"+KodeResep+"', '"+KodeObat+"', '"+Dosis+"', '"+Jumlah+"')";
                            try{
                                //menyiapkan statement untuk di eksekusi
                                PreparedStatement ps4 = (PreparedStatement) connect.prepareStatement(query4);
                                ps4.executeUpdate(query4);
                            }catch(SQLException | HeadlessException e){
                                System.out.println(e);
                            }
                        }
                    }catch(SQLException | HeadlessException e){
                        System.out.println(e);
                   }
                }
            }catch(SQLException | HeadlessException e){
                System.out.println(e);
                JOptionPane.showMessageDialog(null,"Data Gagal Disimpan");
            }finally{
                JOptionPane.showMessageDialog(null,"Data Berhasil Disimpan");
                this.Keluhan.setText(null);
                Diagnosis.setText(null);
                Perawatan.setText(null);
                BeratBadan.setText(null);
                TensiD.setText(null);
                TensiS.setText(null);
            }
    }
