    private void kodeotomatisPemeriksaan(){
        String query = "SELECT * FROM Pemeriksaan ORDER BY NoPemeriksaan DESC ";
        try{
            Connection connect = Koneksi.getKoneksi();//memanggil koneksi
            Statement sttmnt = connect.createStatement();//membuat statement
            ResultSet rslt = sttmnt.executeQuery(query);//menjalanakn query
            
            if (rslt.next()){
                String NoPemeriksaan = rslt.getString("NoPemeriksaan").substring(4,7);
                String AN = "" + (Integer.parseInt(NoPemeriksaan)+1);
                String NOL = "";
                if(AN.length()==1){
                    NOL = "000";
                }else if(AN.length()==2){
                    NOL = "00";
                }else if(AN.length()==3){
                    NOL = "0";
                }else if(AN.length()==4){
                    NOL = "";
                }
                KodePemeriksaan = ("PMR" + NOL + AN);
            }else{
                KodePemeriksaan = ("PMR0001");
            }
        rslt.close();
        connect.close();
        }catch(Exception e){
            System.out.println(e);
        }
    }
