    private void kodeotomatis(){
        String query = "SELECT * FROM pembayaran ORDER BY KodePembayaran DESC ";
        try{
            Connection connect = Koneksi.Koneksi.getKoneksi();//memanggil koneksi
            Statement sttmnt = connect.createStatement();//membuat statement
            ResultSet rslt = sttmnt.executeQuery(query);//menjalanakn query
            
            if (rslt.next()){
                String Id = rslt.getString("KodePembayaran").substring(4,7);
                String AN = "" + (Integer.parseInt(Id)+1);
                String NOL = "";
                if(AN.length()==1){
                    NOL = "000";
                }else if(AN.length()==2){
                    NOL = "00";
                }else if(AN.length()==3){
                    NOL = "0";
                }else if(AN.length()==4){
                    NOL = "";
                }
                txtNopembayaran.setText("PMB" + NOL + AN);
            }else{
                txtNopembayaran.setText("PMB0001");
            }
        rslt.close();
        connect.close();
        }catch(Exception e){
            System.out.println(e);
        }
    }
