    private void AntrianOtomatis(){
        String query = "SELECT * FROM pendaftaran WHERE TglPendaftaran = CURDATE() ORDER BY NoUrut DESC";
        try{
            Connection connect = Koneksi.Koneksi.getKoneksi();//memanggil koneksi
            Statement sttmnt = connect.createStatement();//membuat statement
            ResultSet rslt = sttmnt.executeQuery(query);//menjalanakn query
            
            if (rslt.next()){
                String NoUrut = rslt.getString("NoUrut");
                this.NoUrut.setText(String.valueOf(Integer.parseInt(NoUrut)+1));
            }else{
                this.NoUrut.setText("1");
            }
        rslt.close();
        connect.close();
        }catch(Exception e){
            System.out.println(e);
        }
    }
    private void KodePendaftaranOtomatis(){
        String query = "SELECT * FROM pendaftaran  ORDER BY NoPendaftaran DESC";
        try{
            Connection connect = Koneksi.Koneksi.getKoneksi();//memanggil koneksi
            Statement sttmnt = connect.createStatement();//membuat statement
            ResultSet rslt = sttmnt.executeQuery(query);//menjalanakn query
            
            if (rslt.next()){
                String NoPen = rslt.getString("NoPendaftaran").substring(1);
                String AN = "" + (Integer.parseInt(NoPen)+1);
                String NOL = "";
                if(AN.length()==1){
                    NOL = "000";
                }else if(AN.length()==2){
                    NOL = "00";
                }else if(AN.length()==3){
                    NOL = "0";
                }else if(AN.length()==4){
                    NOL = "";
                }
                NoPendaftaran = ("R" + NOL + AN);
            }else{
                NoPendaftaran = ("R0001");
            }
        rslt.close();
        connect.close();
        }catch(Exception e){
            System.out.println(e);
        }
    }
