    private void tampilData(){
        //untuk mengahapus baris setelah input
        int row = TableDataPasien.getRowCount();
        for(int a = 0 ; a < row ; a++){
            table.removeRow(0);
        }
        
        String query = 
                "SELECT NoPendaftaran,pendaftaran.NoPasien,NamaPas,TglPendaftaran,NoUrut FROM pendaftaran INNER JOIN pasien ON pendaftaran.NoPasien = pasien.NoPasien";
                
        
        try{
            Connection connect = Koneksi.Koneksi.getKoneksi();//memanggil koneksi
            Statement sttmnt = connect.createStatement();//membuat statement
            ResultSet rslt = sttmnt.executeQuery(query);//menjalanakn query
            
            while (rslt.next()){
                //menampung data sementara
                   
                    String noPen = rslt.getString("NoPendaftaran");
                    String noPas = rslt.getString("NoPasien");
                    String namaPas = rslt.getString("NamaPas");
                    String tglPen = rslt.getString("TglPendaftaran");
                    String urut = rslt.getString("NoUrut");
                    
                    
                //masukan semua data kedalam array
                String[] data = {noPen,noPas,namaPas,tglPen,urut};
                //menambahakan baris sesuai dengan data yang tersimpan diarray
                table.addRow(data);
            }
                //mengeset nilai yang ditampung agar muncul di table
                TableDataPasien.setModel(table);
            
        }catch(Exception e){
            System.out.println(e);
        }
       
    }
